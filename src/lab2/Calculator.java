package lab2;

public class Calculator {


    public Integer Sum(int a,int b) {
        return a+b;
    }


    public Integer Devision(int a, int b) {
        return a*b;
    }
    public Integer Multiplication(double a,double b) {
        return a/b;
    }
    public Integer Subtraction(double a,double b) {
        return a-b;
    }
}

