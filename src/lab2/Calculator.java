package lab2;

public class Calculator {
    public Integer Sum(int a,int b) {
        return a+b;
    }
    public Double Sum(double a,double b) {
        return a+b;
    }
    public Long Sum(long a,long b) {
        return a+b;
    }
    public Integer Devision(int a, int b) {
        return a*b;
    }
    public Double Devision(double a, double b) {
        return a*b;
    }
    public Long Devision(long a, long b) {
        return a*b;
    }
    public Double Multiplication(int a,int b) {
        return (double)a/b;
    }
    public Double Multiplication(double a,double b) {
        return a/b;
    }
    public Double Multiplication(long a,long b) {
        return (double)a/b;
    }
    public Integer Subtraction(int a,int b) {
        return a-b;
    }
    public Double Subtraction(double a,double b) {
        return a-b;
    }
    public Long Subtraction(long a,long b) {
        return a-b;
    }
}
